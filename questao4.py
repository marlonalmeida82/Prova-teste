#Progama para mostrar os dias da semana com base no número de 1 a 7

numero_dia =int(input("Digite um número de 1 a 7"))

if numero_dia==1:
    print("Domingo")
elif numero_dia==2:
    print("Segunda Feira")
elif numero_dia==3:
    print("Terça Feira")
elif numero_dia==4:
    print("Quarta Feira")
elif numero_dia==5:
    print("Quinta Feira")
elif numero_dia==6:
    print("Sexta Feira")
elif numero_dia==7:
    print("Sábado do Senhor")

else:
    print("Número invalido.Digite um número entre 1 e 7")