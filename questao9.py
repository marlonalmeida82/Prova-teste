# Criando uma tupla
tupla_demo = ("Eu", "Tu", "Ele")

# Iterando sobre os elementos da tupla e imprimindo-os
for elemento in tupla_demo:
    print(elemento)

# As tuplas são imutáveis, ou seja, não podem ser modificadas após a criação.
# Você não pode adicionar, remover ou alterar elementos em uma tupla.
# Portanto, a tupla "tupla_demo" não pode ser modificada.